let registered = false

/**
 * Register every slash command into the slash registry AND the global command
 * palette (shared definitions). Idempotent.
 */
export function registerSlashCommands(): void {
  if (registered) return
  registered = true
  for (const command of SLASH_COMMANDS) {
    registerSlashCommand(command)
    // Mirror into the command palette so Ctrl/Cmd+K can run inserts too.
    registerCommand({
      id: `slash:${command.id}`,
      title: command.title,
      category: command.category,
      shortcut: command.shortcut,
      isAvailable: () => {
        if (command.disabledReason) return false
        if (command.global) return useVault.getState().status === 'ready'
        const tabs = useTabs.getState()
        const active = tabs.tabs.find((t) => t.id === tabs.activeId)
        return active?.type === 'note'
      },
      run: () => {
        const view = getActiveView()
        if (command.global) {
          command.run({ view: view as EditorView })
          return
        }
        if (view) command.run({ view })
      },
    })
  }
}
