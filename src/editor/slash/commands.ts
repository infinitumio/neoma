/**
 * The neoma slash-command set. Every command inserts real, portable Markdown
 * (or opens a real modal / runs a real app command) — there are no fake or
 * placeholder actions. AI commands are present but disabled until an AI
 * plugin is installed, and never require the network.
 *
 * These are the single source of truth: they register into the slash registry
 * AND (via registerSlashCommands) into the global command palette, so `/` and
 * `Ctrl/Cmd+K` share identical definitions.
 */
